#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	char	*s;
	char	*d;

	s = (char *)src;
	d = (char *)dest;
	if (s > d)
		return (ft_memcpy(d, s, n));
	while (n--)
		d[n] = s[n];
	return (dest);
}
